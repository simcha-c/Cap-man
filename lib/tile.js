export const calculateEatingPos = (pos) => {
  let x = Math.floor((pos[0] - 16) / (560 / 28));
  let y = Math.floor((pos[1] - 16) / (660 / 31));
  return [x, y];
};

export const calculateMovingPos = (pos) => {
  let x = Math.ceil((pos[0] - 16) / (560 / 28));
  let y = Math.ceil((pos[1] - 16) / (660 / 31));
  return [x, y];
};

export const getPosInfo = (pos) => {
  return grid[pos[1]][pos[0]];
};

export const getNextPosInfo = (pos, dir) => {
  let col = pos[0] + dir[0];
  let row = pos[1] + dir[1];
  return grid[row][col];
};

export const changeTile = (pos, newVal) => {
  grid[pos[1]][pos[0]] = newVal;
};


